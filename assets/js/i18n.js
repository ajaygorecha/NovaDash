const I18n = {
  currentLang: localStorage.getItem('language') || 'en',

  init() {
    this.applyTranslations();
    this.setupLanguageSelector();
  },

  applyTranslations() {
    const elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(el => {
      const key = el.dataset.translate;
      const translation = this.getTranslation(key);
      
      if (translation) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = translation;
        } else {
          el.textContent = translation;
        }
      }
    });

    this.setLangDirection();
  },

  getTranslation(key) {
    const keys = key.split('.');
    let value = translations[this.currentLang];
    
    for (const k of keys) {
      if (value && value[k]) {
        value = value[k];
      } else {
        return key;
      }
    }
    
    return value;
  },

  setLanguage(lang) {
    if (translations[lang]) {
      this.currentLang = lang;
      localStorage.setItem('language', lang);
      this.applyTranslations();
      this.updateActiveLanguage();
      this.setLangDirection();
    }
  },

  setLangDirection() {
    if (this.currentLang === 'ar') {
      document.documentElement.setAttribute('dir', 'rtl');
      document.documentElement.setAttribute('lang', 'ar');
    } else {
      document.documentElement.setAttribute('dir', 'ltr');
      document.documentElement.setAttribute('lang', this.currentLang);
    }
  },

  setupLanguageSelector() {
    const selector = document.querySelector('.language-selector');
    const options = document.querySelectorAll('.language-option');
    
    if (selector) {
      selector.addEventListener('click', () => {
        const dropdown = selector.nextElementSibling;
        if (dropdown) {
          dropdown.classList.toggle('show');
        }
      });
    }

    options.forEach(option => {
      option.addEventListener('click', () => {
        const lang = option.dataset.lang;
        this.setLanguage(lang);
        
        const dropdown = document.querySelector('.language-dropdown');
        if (dropdown) dropdown.classList.remove('show');
      });
    });

    this.updateActiveLanguage();

    document.addEventListener('click', (e) => {
      if (!e.target.closest('.language-selector')) {
        const dropdowns = document.querySelectorAll('.language-dropdown.show');
        dropdowns.forEach(d => d.classList.remove('show'));
      }
    });
  },

  updateActiveLanguage() {
    const currentLangEl = document.querySelector('.current-language');
    const options = document.querySelectorAll('.language-option');
    
    if (currentLangEl) {
      const langData = {
        en: { name: 'English', flag: '🇺🇸' },
        hi: { name: 'हिंदी', flag: '🇮🇳' },
        ar: { name: 'العربية', flag: '🇸🇦' }
      };
      
      if (langData[this.currentLang]) {
        currentLangEl.innerHTML = `${langData[this.currentLang].flag} ${langData[this.currentLang].name}`;
      }
    }

    options.forEach(option => {
      if (option.dataset.lang === this.currentLang) {
        option.classList.add('active');
      } else {
        option.classList.remove('active');
      }
    });
  }
};

document.addEventListener('DOMContentLoaded', () => I18n.init());